clc, clear; 

% puzzle is a test matrix representing an unsolved sudoku board with zeros for
% blanks. In the future, input is likely to be parsed and then the puzzle array 
% created by this program.
puzzle = [0 0 0 2 6 0 7 0 1;...
          6 8 0 0 7 0 0 9 0;...
          1 9 0 0 0 4 5 0 0;...
          8 2 0 1 0 0 0 4 0;...
          0 0 4 6 0 2 9 0 0;...
          0 5 0 0 0 3 0 2 8;...
          0 0 9 3 0 0 0 7 4;...
          0 4 0 0 5 0 0 3 6;...
          7 0 3 0 1 8 0 0 0];

% I sum the columns and rows here to see if they add up to 45. Adding 1 through
% 9 together equals 45, so in the late stages these sums will be useful.
columnSums = sum(puzzle);
rowSums = sum(puzzle');

% nonetSums produces a 3x3 array of sums, the sums that one would get by adding
% the values in a 3x3 block of a normal sudoku board.
nonetSums = [sum(sum(puzzle(1:3, 1:3))) ...
             sum(sum(puzzle(4:6, 1:3))) ...
             sum(sum(puzzle(7:9, 1:3)));...
             sum(sum(puzzle(1:3, 4:6))) ...
             sum(sum(puzzle(4:6, 4:6))) ...
             sum(sum(puzzle(7:9, 4:6)));...
             sum(sum(puzzle(1:3, 7:9))) ...
             sum(sum(puzzle(4:6, 7:9))) ...
             sum(sum(puzzle(7:9, 7:9)))];
